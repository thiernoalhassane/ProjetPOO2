    public int selectionligne() {
        int ligne = table.getSelectedRow();
        if (ligne == -1) {
            JOptionPane.showMessageDialog(null, "vous n'avez pas selectionner de ligne");
            flag = false;
            return ligne;
        } else {
            String typeue = table.getValueAt(ligne, 3).toString();
            textue1.setText(typeue);
            String nomue = table.getValueAt(ligne, 1).toString();
            textue.setText(nomue);
            String ecue = table.getValueAt(ligne, 2).toString();
            Txtecue.setText(ecue);            
            flag = true;
            return ligne;
        }
    }
    

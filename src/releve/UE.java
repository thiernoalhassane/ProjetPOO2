    public static Object[][] affichagedonnee() {
        int i = 0;
        Object[][] data = null;
        try {

            String requete = "select  idUE,  NomUE , NomEcue , NomType  from ue u , typeue t , ecue e where u.UeCodeEcue = e.CodeEcue and u.UeidTypeUe = t.idTypeUe";
            ResultSet resultat;
            resultat = connection.interroger(requete);
            while (resultat.next()) {
                i++;//nombre de ligne de la table
            }
            data = new Object[i][4];
            resultat.beforeFirst();// me positionne avant la premiere ligne de mon resultat
            int k = 0;
            while (resultat.next()) {
                data[k][0] = resultat.getInt("idUE");
                data[k][1] = resultat.getString("NomUE");
                data[k][2] = resultat.getString("NomEcue");
                data[k][3] = resultat.getString("NomType");                
                k++;
            }
        } catch (SQLException e) {
            System.out.println("La copie de la table dans data a échoué");
        }
        return data;
    }
    

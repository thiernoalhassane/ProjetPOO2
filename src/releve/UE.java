    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int g = table.getSelectedRow();
        int p = Integer.parseInt(table.getValueAt(g, 0).toString());
        int reponse = JOptionPane.showConfirmDialog(null, "Voulez vous supprimer?");

        if (reponse == 0) {
            String requete = "Delete from ue where idUE = '" + p + "'";            
            connection.maj(requete);            
            actualiser();
            iniatiliser();
        } else {
            JOptionPane.showMessageDialog(null, "Echec de la suppression");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

